/********************************************************************************************************************************
														LDL_Result
********************************************************************************************************************************/
class LDL_Result
{
public:
	enum
	{
		Max = 64
	};

	LDL_Result()
	{
		Clear();
	}

	void Clear()
	{
		_Ok = true;
		memset(&_Message, 0, Max);
	}

	bool Ok()
	{
		return _Ok;
	}

	const char* Message()
	{
		return _Message;
	}

	void Message(const char* message)
	{
		Clear();

		_Ok = false;
		strcpy(_Message, message);
	}
private:
	bool _Ok;
	char _Message[Max];
};

void LDL_Abort(const char* message, const char* detail)
{
	printf("%s %s/n", message, detail);
	abort();
}
